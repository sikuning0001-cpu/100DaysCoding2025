public class Day6 {
    public static void main(String[] args) {
        // float → angka desimal presisi 7 digit
        float x = 3.14f;  // pakai 'f' di akhir biar dikenali float

        // double → angka desimal presisi 15 digit
        double y = 3.14159265359;

        // Cetak hasil
        System.out.println("float: " + x);
        System.out.println("double: " + y);
    }
}