public class Day30OperatorKurangLebihSama {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;

        System.out.println("Nilai a = " + a);
        System.out.println("Nilai b = " + b + "\n");

        System.out.println("Apakah a >= b ? " + (a >= b));
        System.out.println("Apakah a <= b ? " + (a <= b));
    }
}