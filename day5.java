public class FORISC100DAYSCODING {

    public static void main(String[] args) {
        byte a = 100;
        short b = 2000;
        int c = 3000;
        long d = 1000000000L ;
        
        System.out.println("byte \t:" + a);
        System.out.println("short \t:" + b);
        System.out.println("int \t:" + c);
        System.out.println("long \t:" + d);
    }

}